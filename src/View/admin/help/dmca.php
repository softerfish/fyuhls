    <p class="guide-purpose mb-4">Use DMCA for copyright and ownership complaints where you need a legal takedown record, not just a generic abuse moderation decision.</p>

    <h6 class="fw-bold text-dark border-bottom pb-2 mb-3">How To Handle A Claim</h6>
    <ol class="guide-steps mb-4">
        <li><strong>Validate the notice:</strong> Confirm the claimant identity, the allegedly infringed work, the hosted file URL, and enough detail to justify the request.</li>
        <li><strong>Inspect the hosted file:</strong> Open the file link or the related record in <a href="/admin/files" class="guide-action-link">Files</a> to make sure the complaint actually matches the hosted content.</li>
        <li><strong>Takedown valid notices promptly:</strong> Delete or disable the file using the admin workflow so the database and storage object stay aligned.</li>
        <li><strong>Reject incomplete or false claims:</strong> If the notice is missing key information or targets the wrong file, reject it instead of silently removing content.</li>
        <li><strong>Keep the audit trail:</strong> Preserve the DMCA row, timestamps, and any related communication so you can track future disputes or counter-notices.</li>
    </ol>

    <h6 class="fw-bold text-dark border-bottom pb-2 mb-3">Counter-Notice And Follow-Up</h6>
    <ul class="extra-small text-muted mb-4">
        <li class="mb-2"><strong>User disputes:</strong> If an uploader appeals, route them through your normal support channel or <a href="/admin/contacts" class="guide-action-link">Contacts</a> so the communication stays associated with the claim.</li>
        <li class="mb-2"><strong>Multiple notices for one file:</strong> Keep the related decisions consistent so the same file is not half-processed across rows.</li>
        <li><strong>Separate from abuse:</strong> Malware or phishing should stay in <a href="/admin/abuse-reports" class="guide-action-link">Abuse Reports</a>; this page is for copyright-specific workflow.</li>
    </ul>

    <div class="alert alert-warning border-0 shadow-sm small">
        <strong>Legal Guard:</strong> A consistent, documented workflow matters. Act quickly on valid notices, keep records, and avoid off-record deletions that leave no trace in the DMCA queue.
    </div>
