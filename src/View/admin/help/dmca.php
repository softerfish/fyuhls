<div class="p-1">
    <p class="guide-purpose mb-4">Protect your platform's legal standing by processing DMCA takedown requests accurately and swiftly.</p>

    <h6 class="fw-bold text-dark border-bottom pb-2 mb-3">How to Handle Claims:</h6>
    <ol class="guide-steps mb-4">
        <li>
            <strong>Verify Ownership:</strong> Review the documentation provided by the reporter. Ensure they are the legitimate rights holder or an authorized agent.
        </li>
        <li>
            <strong>Inspect the Link:</strong> Click the reported file link to verify it matches the copyrighted content described in the claim.
        </li>
        <li>
            <strong>Execute Takedown:</strong> If the claim is valid, click <strong>Delete File</strong>. This removes it from all nodes and notifies the uploader.
        </li>
        <li>
            <strong>Reject False Claims:</strong> If a report is clearly fraudulent or lacks required info, mark it as <strong>Rejected</strong> to protect your users.
        </li>
    </ol>

    <h6 class="fw-bold text-dark small text-uppercase mb-2">Common Pitfalls:</h6>
    <ul class="extra-small text-muted mb-4">
        <li class="mb-2"><strong>Bulk Logic:</strong> You can select multiple reports for the same content and process them all with a single click.</li>
        <li><strong>User Appeals:</strong> If a user believes a takedown was in error, they must contact you via the 
        <a href="/admin/contacts" class="guide-action-link">Contact Center</a> with a counter-notice.</li>
    </ul>

    <div class="alert alert-warning border-0 shadow-sm small">
        <strong>Legal Guard:</strong> Maintaining a "Safe Harbor" requires you to act expeditiously once a valid notification is received.
    </div>
</div>
