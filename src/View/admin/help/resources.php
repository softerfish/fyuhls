    <h6 class="fw-bold text-dark border-bottom pb-2 mb-3">What Belongs Here</h6>
    <ul class="mb-4">
        <li><strong>Affiliates:</strong> Monetization and referral programs you actively recommend to Fyuhls operators, with a short note on why each one is useful.</li>
        <li><strong>Supporters:</strong> Companies that directly supported the script or helped make features possible.</li>
        <li><strong>Operational resources:</strong> Services that improve fraud controls, security, payments, hosting, or launch readiness.</li>
        <li><strong>Future host partnerships:</strong> Preferred hosts or infrastructure partners you want new operators to consider.</li>
    </ul>

    <h6 class="fw-bold text-dark border-bottom pb-2 mb-3">How To Use The Page</h6>
    <ol class="guide-steps mb-4">
        <li><strong>Add a category first:</strong> Group listings into sections like Affiliates, Technology Partners, and Hosting Partners so the page stays easy to scan.</li>
        <li><strong>Keep sections intentional:</strong> Split listings into clear groups such as Technology Partners and Hosting Partners so the page can grow without becoming noisy.</li>
        <li><strong>Explain practical value:</strong> Each listing should say how that company helps a new file hosting website, not just who they are.</li>
        <li><strong>Link to the real site:</strong> Make it easy for admins to click through and evaluate the service directly.</li>
        <li><strong>Keep the sponsor note visible:</strong> The intro should always explain that these companies helped support the script and where to email for a future listing.</li>
    </ol>

