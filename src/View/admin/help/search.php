<div class="p-1">
    <p class="guide-purpose mb-4">Use Admin Search for fast lookups across users and files when you already know part of the username, email, file short ID, or filename.</p>

    <h6 class="fw-bold text-dark border-bottom pb-2 mb-3">What Search Supports</h6>
    <ul class="extra-small text-muted mb-4">
        <li class="mb-2"><strong>Direct identifiers:</strong> User ID, file ID, file short ID, and exact usernames or emails.</li>
        <li class="mb-2"><strong>Partial matching:</strong> Username, email, and filename searches can match substrings such as <code>admin</code> or part of a long file title.</li>
        <li><strong>Jump-off workflow:</strong> Results can send you into the matching admin list or directly to the public file page for verification.</li>
    </ul>

    <h6 class="fw-bold text-dark border-bottom pb-2 mb-3">Best Use</h6>
    <ol class="guide-steps mb-4">
        <li><strong>Start broad:</strong> Search a partial username, email, or filename first when you are not sure of the exact ID.</li>
        <li><strong>Use IDs when possible:</strong> Numeric IDs and file short IDs are faster and less ambiguous.</li>
        <li><strong>Confirm on the destination page:</strong> Search is a locator, not the final source of truth for moderation or billing actions.</li>
    </ol>

    <div class="alert alert-light border-0 shadow-sm small">
        <strong>Tip:</strong> If a user says “I can’t find my file,” try the file short ID first, then partial filename, then their username. That order usually gets you to the right result the fastest.
    </div>
